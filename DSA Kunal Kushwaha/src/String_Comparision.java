import java.io.StringReader;

public class String_Comparision {
    public static void main(String[] args) {
        System.out.println(750*2600);
        Integer num = new Integer(45);
        System.out.println(num.toString());
        System.out.println(num.equals(67)+"\n\n\n");


        System.out.println(56);
        String a = "Ravi";
        String b = "Ravi";
        System.out.println(a == b);
        System.out.println(a.equals(b));
        System.out.println(a.compareTo(b)+"\n\n");


        String str1 = new String("Ravi");
        String str2 = new String("Ravi");
        System.out.println(str1 == str2);
        System.out.println(str1.equals(str2));
        System.out.println(str1.compareTo(str2));


    }
}
