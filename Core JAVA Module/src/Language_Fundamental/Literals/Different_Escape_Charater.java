package Language_Fundamental.Literals;

public class Different_Escape_Charater {
    public static void main(String[] args) {
        System.out.println("Ravi\nKumarGupta");
        System.out.println("Ravi\tKumarGupta");
        System.out.println("Ravi\rKumarGupta");
        System.out.println("Ravi\bKumarGupta");
        System.out.println("Ravi\fKumarGupta");
        System.out.println("Ravi\'KumarGupta");
        System.out.println("Ravi\"KumarGupta");


    }
}
