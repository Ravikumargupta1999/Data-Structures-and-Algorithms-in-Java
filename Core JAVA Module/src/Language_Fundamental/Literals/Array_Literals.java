package Language_Fundamental.Literals;

public class Array_Literals {
    public static void main(String[] args) {
        int[] arr = new int[67];
        System.out.println(arr.getClass());
        int[][] arr2 = new int[67][];
        System.out.println(arr2.getClass());
        int[] arr3 = new int[0];
        System.out.println(arr3);


    }
}
