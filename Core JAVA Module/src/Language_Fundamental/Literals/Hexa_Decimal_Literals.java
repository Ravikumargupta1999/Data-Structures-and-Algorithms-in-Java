package Language_Fundamental.Literals;

public class Hexa_Decimal_Literals {
    public static void main(String[] args) {
        int x = 0X90;
        System.out.println(x);
        System.out.println(0X15);
        int cal = 0X11;
        System.out.println(cal);
        cal = 0X10;
        System.out.println(cal);
    }
}
